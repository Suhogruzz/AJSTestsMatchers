import sortPlayers from "../testmatchers";


const players = [
  { name: 'мечник', health: 10 },
  { name: 'маг', health: 100 },
  { name: 'лучник', health: 80 },
];

const expected = [
  { name: 'маг', health: 100 },
  { name: 'лучник', health: 80 },
  { name: 'мечник', health: 10 },
];

test('sorted players:', () => {
  let result = sortPlayers(players);
  expect(result).toEqual(expected);
})